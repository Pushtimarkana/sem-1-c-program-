#include<stdio.h>
void main(){
	int a;
	printf("enter decimal number:");
	scanf("%d",&a);
	while(a!=0){
		if(a>=1000){
			printf("M");
			a=a-1000;
		}
		if(a>=900){
			printf("CM");
			a=a-900;
		}
		if(a>=500){
			printf("D");
			a=a-500;
		}
		if(a>=400){
			printf("CD");
			a=a-400;
		}
		if(a>=100){
			printf("C");
			a=a-100;
		}
		if(a>=90){
			printf("XC");
			a=a-90;
		}
		if(a>=50){
			printf("L");
			a=a-50;
		}
		if(a>=40){
			printf("XL");
			a=a-40;
		}
		if(a>=10){
			printf("X");
			a=a-10;
		}
		if(a>=9){
			printf("IX");
			a=a-9;
		}
		if(a>=5){
			printf("V");
			a=a-5;
		}
		if(a>=4){
			printf("IV");
			a=a-4;
		}
		if(a>=1){
			printf("I");
			a=a-1;
		}
		else{
		}
	}
}
